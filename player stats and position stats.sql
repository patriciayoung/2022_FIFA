CREATE TABLE PLAYER_INFO (
	PLAYER VARCHAR(50),
	TEAM VARCHAR(25),
	AGE INT
);


CREATE TABLE POSITION (
	POSITION VARCHAR(5),
	TEAM VARCHAR(25)
);


CREATE TABLE PLAYER_STATS (
	TOUCHES INT, 
	DRIBBLES_COMPLETED INT, 
	GOALS INT, 
	SHOTS INT, 
	SHOTS_ON_TARGET INT, 
	PASSES_COMPLETED INT, 
	PASSES INT, 
	ASSISTS INT, 
	TACKLES_WON INT
);




